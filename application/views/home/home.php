    $(document).ready(function () {
        $( '#fromWhen' ).datepicker({
            minDate: 0,
            onSelect: checkDate
        });

        function checkDate(start_date, el) {
            $( '#fromTo' ).datepicker('option', 'disabled', false);
            $( '#fromTO' ).datepicker('option', 'minDate', start_date);
        }

        $( '#fromTo' ).datepicker({
            numberOfMonths: 1,
            minDate: 0
        });
        $( '#fromTo' ).datepicker('option', 'disabled', true);
          
        $('.bxslider').bxSlider();
        
        vertical_position(".travellers-image");
        
        
        var $imgs = $('#gallery img');
        var $buttons = $('#buttons');
        var tagged = {};

        $imgs.each(function() {
            var img = this;
            var tags = $(this).data('tags');

            if (tags) {
                tags.split(',').forEach(function(tagName) {
                if (tagged[tagName] == null) {
                    tagged[tagName] = [];
                }
                    tagged[tagName].push(img);
                })
            }
        })

        $('<button/>', {
            text: 'Show All',
            class: 'active',
            click: function() {
                $(this)
                .addClass('active')
                .siblings()
                .removeClass('active');
                $imgs.show();
            }
        }).appendTo($buttons);

        $.each(tagged, function(tagName) {
            var $n = $(tagged[tagName]).length;
            $('<button/>', {
                text: tagName + '(' + $n + ')',
                click: function() {
                $(this)
                .addClass('active')
                .siblings()
                .removeClass('active');
                $imgs
                .hide()
                .filter(tagged[tagName])
                .show();
                }
            }).appendTo($buttons);
        });

        
    });
    
    $(window).resize(function(){
        vertical_position(".travellers-image");
    });

</script>